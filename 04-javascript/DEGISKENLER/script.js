// değişken tanımlama var ile yapılır
// değişken tanımlarken türkçe karakter kullanmamalı ve kelimeler arası boşluk bırakılmamalı ve - kullanılmamalı _ kuullanılarbilir
// değişken adının başına sayı yazılmaz sayı ile başlayamaz ancak sonda sayı olabilir
// değişken tanımlarken ilk kelime küçük harfle başlar ikinci kelimevarsa büyük harfle başlar


var maasAli = 7000;
// bunu yaptıktan sonra 7000 gördüğümüz her yere maasAli yazabiliriz
var maasCan = 6000;
// 600 gördüğümüz her yere maasCan yazabiliriz
// bu sayede yarın öbür gün bu değerlerde güncelleme yapmak istediğimde tek bir yerde değişken tanımlama kısmında değişiklik yaparak her yerde kolayca değişiklik yapabilirim

// zam oranını yapalım
// ve artık 0.3 gördüğüm her yere zam yazabilirim
var zam = 0.3;

console.log(maasAli + (maasAli * zam)) //alinin maaşı
console.log(maasCan + (maasCan * zam)) //canın maaşı

